select P.NAZWISKO, P.ETAT, P.ID_ZESP, Z.NAZWA
from PRACOWNICY as P, ZESPOLY as Z
where P.ID_ZESP = Z.ID_ZESP
order by P.NAZWISKO;

SELECT NAZWISKO, CONCAT(SUBSTRING(ETAT, 1,2), ID_PRAC) AS KOD FROM PRACOWNICY;

SELECT NAZWISKO, REPLACE(REPLACE(REPLACE(NAZWISKO, "I", "X"), "M", "X"),"K", "X") FROM PRACOWNICY;

SELECT NAZWISKO, PLACA_POD * 1.15 AS PODWYZKA FROM PRACOWNICY;

SELECT NAZWISKO, PLACA_POD, PLACA_POD*0.2 AS INWESTYCJA, PLACA_POD*0.2*POW(1.1, 10) AS KAPITAL, (PLACA_POD*0.2*POW(1.1, 10) - PLACA_POD*0.2) AS ZYSK FROM PRACOWNICY;

SELECT NAZWISKO, ZATRUDNIONY, (2000 - YEAR(ZATRUDNIONY) ) AS STAZ_W_2000 FROM PRACOWNICY;

SELECT NAZWISKO, DATE_FORMAT(ZATRUDNIONY, "%M %d %Y") AS DATA_ZATRUDNIENIA FROM PRACOWNICY WHERE ID_ZESP=20;

SELECT DAYNAME(CURRENT_DATE) AS DZIS;

SELECT ID_ZESP, NAZWA, ADRES FROM ZESPOLY;

